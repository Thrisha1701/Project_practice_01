package basic_008_opps;

public class Student {
	private String name;
	private String number;
	private int age;
	private double pocket_money;
	
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public double getPocket_money() {
		return pocket_money;
	}
	public void setPocket_money(double pocket_money) {
		this.pocket_money = pocket_money;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
}
